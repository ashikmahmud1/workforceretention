/**
 * An object used to get staticPage information from the server
 */
export class Page {
    // The number of elements in the staticPage
    size: number = 0;
    // The total number of elements
    totalElements: number = 0;
    // The total number of pages
    totalPages: number = 0;
    // The current staticPage number
    pageNumber: number = 0;
}